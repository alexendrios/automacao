package br.com.bb.mdc.testesAutomatizadosEntidade;

public class IdentificacaoProtocolo {

	private String headear;
	private String trailler;
	private String dataMovimentoHeadear;
	private String nomeArquivoHeadear;
	private String remessaHeadear;
	private String dataMovimentoTrailler;
	private String nomeArquivoTrailler;
	private String remessaTrailler;

	public String getHeadear() {
		return headear;
	}

	public void setHeadear(String headear) {
		this.headear = headear;
	}

	public String getTrailler() {
		return trailler;
	}

	public void setTrailler(String trailler) {
		this.trailler = trailler;
	}

	public String getDataMovimentoHeadear() {
		return dataMovimentoHeadear;
	}

	public void setDataMovimentoHeadear(String dataMovimentoHeadear) {
		this.dataMovimentoHeadear = dataMovimentoHeadear;
	}

	public String getNomeArquivoHeadear() {
		return nomeArquivoHeadear;
	}

	public void setNomeArquivoHeadear(String nomeArquivoHeadear) {
		this.nomeArquivoHeadear = nomeArquivoHeadear;
	}

	public String getRemessaHeadear() {
		return remessaHeadear;
	}

	public void setRemessaHeadear(String remessaHeadear) {
		this.remessaHeadear = remessaHeadear;
	}

	public String getDataMovimentoTrailler() {
		return dataMovimentoTrailler;
	}

	public void setDataMovimentoTrailler(String dataMovimentoTrailler) {
		this.dataMovimentoTrailler = dataMovimentoTrailler;
	}

	public String getNomeArquivoTrailler() {
		return nomeArquivoTrailler;
	}

	public void setNomeArquivoTrailler(String nomeArquivoTrailler) {
		this.nomeArquivoTrailler = nomeArquivoTrailler;
	}

	public String getRemessaTrailler() {
		return remessaTrailler;
	}

	public void setRemessaTrailler(String remessaTrailler) {
		this.remessaTrailler = remessaTrailler;
	}

}
